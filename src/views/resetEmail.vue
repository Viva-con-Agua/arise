<template>
  <VcAFrame>
    <VcAColumn>
      <VcABox :first="true" :title="$t('reset.Email.title')">
        <el-form
          :model="resetFormEmail"
          :ref="resetFormEmail"
          :rules="rules"
	  @keyup.enter.native="submitForm(resetFormEMail)"
          status-icon>
          <el-form-item
            :label="$t('reset.label.newEmail')"
            prop="email">
            <div id="pw">
              <el-input
                v-model.trim="resetFormEmail.email"
                type="email"/>
            </div>
          </el-form-item>
          <el-form-item
            :label="$t('reset.label.checkEmail')"
            prop="checkPass">
            <el-input
              v-model.trim="resetFormEmail.checkEmail"
              type="email"/>
          </el-form-item>
        </el-form>
        <button
                ref="resetIt"
                class="vca-button-primary vca-full-width"
                @click.prevent="submitForm(resetFormEmail)">
          {{ $t('options.resetIt') }}
        </button>
      </VcABox>
    </VcAColumn>
  </VcAFrame>
</template>

<script>
  import Vue from 'vue'
  import Password from 'vue-password-strength-meter';
  import axios from 'axios'
  import VueAxios from 'vue-axios'
  import { VcAFrame, VcAColumn, VcABox } from 'vca-widget-base'
  import 'vca-widget-base/dist/vca-widget-base.css'
  import {
    Button,
    Form,
    FormItem,
    Notification,
    Input
  } from 'element-ui'


  Vue.use(VueAxios, axios);
  Vue.use(Button);
  Vue.use(Form);
  Vue.use(FormItem);
  Vue.use(Notification);
  Vue.use(Input);

  Notification.closeAll();

  export default {
        name: "ResetPassword",
        components: { VcAFrame, VcAColumn, VcABox },

        data() {
            var checkPass = (rule, value, callback) => {
                if (value === '') {
                    callback(new Error(this.$t('signup.error.email.empty')));
                } else if (value !== this.resetFormPassword.password) {
                    callback(new Error(this.$t('signup.error.email.unequal')));
                } else {
                    callback();
                }
            };

            return {
              resetFormEmail: {
                email: '',
                checkEmail: ''
              },
              suggestions: [],

              rules: {
                email: [
                   {required: true, message: this.$t('validationError.email'), trigger: 'blur'},
                        {pattern:/^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/, message: this.$t('inputSample.email'), trigger: 'blur' }
                 ],
                checkEmail: [
                    { required: true, validator: checkPass, trigger: 'blur' }
                ]
              }
            }
        },
        methods: {
            messageOpen(title, message, type) {
                if(typeof title !== 'undefined' && typeof message !== 'undefined' && typeof type !== 'undefined') {
                    Notification({
                        title: title,
                        message: message,
                        type: type,
                        duration: 3000
                    });
                }
            },
            disableButton() {
                this.$refs.resetIt.disabled = true
            },
          submitForm(resetFormEmail) {
            var that = this;
            this.$refs[resetFormEmail].validate((valid) => {
              if(valid) {
                this.axios
                  .post('/drops/webapp/email/reset/' + this.$route.params.token, {
                    email: that.resetFormEmail.email,
                    checkEmail: that.resetFormEmail.checkEmail
                  })
                  .then(function(response) {
                      that.disableButton()
                      that.messageOpen(that.$t('reset.EmailDone.title'), that.$t('reset.EmailDone.description'), 'success')
                      setTimeout( () => that.$router.push({path: '/signin'}), 3000);
                  })
                  .catch(function(error) {
                    switch (error.response.status) {
                      case 401:
                        that.messageOpen(that.$t('reset.EmailInstructions.errorResponse'), error.response.data.msg, "error");
                        break;
                      case 404:
                        that.messageOpen(that.$t('reset.EmailInstructions.errorResponse'), error.response.data.msg, "error");
                        break;
                    }
                  })
                  .finally(() => this.loading = false);
              }
            });
          },
          resetForm(resetFormEmail) {
            this.$refs[resetFormEmail].resetFields();
          },
          showFeedback ({suggestions, warning}) {
              this.suggestions = suggestions;
          },
          showScore (score) {
            // console.log('ðŸ’¯', score);
          },
        }
    }
</script>
